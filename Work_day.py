# b каалагандай 1-7 чейинки маанилерди кабыл алат.
# Эгерде 1-5 чейин болсо иш куну, 6 болсо кыска кун ал эми
# 7 болсо дем алыш деген билдируу чыксын.

b=int(input("каалагандай санды киргизиниз: "))
if b>=1 and b<=5:
    print("Иш кун")
elif b<=6:
    print("Кыска иш кун")
elif b<=7:
    print("Дем алыш")
else:
    print("Мындай кун жок")

